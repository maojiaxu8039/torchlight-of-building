const fs = require("fs");

const translations = JSON.parse(
  fs.readFileSync(
    "src/data/translated-affixes/merged-all-translations.json",
    "utf8",
  ),
);

const newTranslations = {
  "+60% Ailment Damage Enhancement": "+60% 异常状态伤害加深",
  "+35% additional Defense gained from Shield": "额外 +35% 从护盾获得的防御",
  "+61% Warcry Effect": "+61% 战吼效果",
  "+100% Critical Strike Damage": "+100% 暴击伤害",
  "+200% Critical Strike Rating": "+200% 暴击值",
};

let added = 0;
Object.entries(newTranslations).forEach((entry) => {
  if (!translations[entry[0]]) {
    translations[entry[0]] = entry[1];
    added++;
  }
});

const sorted = Object.entries(translations).sort(
  (a, b) => b[0].length - a[0].length,
);
const result = {};
sorted.forEach((e) => {
  result[e[0]] = e[1];
});
fs.writeFileSync(
  "src/data/translated-affixes/merged-all-translations.json",
  JSON.stringify(result, null, 2),
);

console.log("已添加 " + added + " 个翻译");
console.log("总计: " + Object.keys(result).length + " 条翻译");
